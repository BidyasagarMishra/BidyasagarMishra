#Unpack a Collection
#If you have a collection of values in a list, tuple etc. Python allows you to extract the values into variables. This is called unpacking.
print("unpack a collection")
fruits=["apple","banana","cherry"]
d,e,f=fruits
print(d)
print(e)
print(f)